    public void mostrarAlumno() {
        jTextArea1.setText("");
        for (Alumno a : listaAlumnos) {
            jTextArea1.append(a.toString() + "\n");
        }
    }

    public void aniadirAlumno(Alumno a1) {
        listaAlumnos.add(a1);
    }

    public void eliminarAlumno(Alumno a1) {
        for (Alumno a : listaAlumnos) {
            if (a.equals(a1)) {
                listaAlumnos.remove(a1);
            }
        }
    }

